public class Main
{
    public static void main(String[] args)
    {
        //Implicit Casting
        int p = 100;
        double q = p; //int to double
        System.out.println(q);

        //Explicit Casting
        double x = 99.99;
        int y = (int)x; //double to int
        System.out.println(y);
    }
}